/**
 * Handles `("market", "sold")` → (buyer: Address, seller: Address, ship_id: u64,
 *                                  price: i128, royalty: i128, seller_proceeds: i128)
 */
export function handleMarketSold(event: any): void {
  const now = eventTimestamp();
  const buyer = event.parameters[0].value.toAddress().toHexString();
  const seller = event.parameters[1].value.toAddress().toHexString();
  const shipId = event.parameters[2].value.toU64().toString();
  const price = event.parameters[3].value.toBigInt();
  const royalty = event.parameters[4].value.toBigInt();
  const sellerProceeds = event.parameters[5].value.toBigInt();

  ensurePlayer(buyer, now);

  const listingId = makeId("listing", seller, shipId, "");
  // Search for active listing — in production, iterate or use derived store queries.
  // For this handler, we reconstruct from event data and update.
  const listing = new MarketplaceListing(listingId);
  listing.ship = shipId;
  listing.seller = seller;
  listing.price = price;
  listing.active = false;
  listing.listedAt = now;
  listing.closedAt = now;
  listing.buyer = buyer;
  listing.royaltyAmount = royalty;
  listing.sellerProceeds = sellerProceeds;
  listing.save();

  // Update ship ownership
  const ship = Ship.load(shipId);
  if (ship != null) {
    ship.owner = buyer;
    ship.save();
  }
}

