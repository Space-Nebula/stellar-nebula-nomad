/**
 * Handles `("dao", "vote")` → (proposal_id: u64, voter: Address, power: i128)
 */
export function handleDAOVote(event: any): void {
  // DAO vote events don't carry direction; default to "For" — direction
  // can be enriched by querying the on-chain vote record.
  const now = eventTimestamp();
  const proposalId = event.parameters[0].value.toU64().toString();
  const voter = event.parameters[1].value.toAddress().toHexString();
  const power = event.parameters[2].value.toBigInt();

  ensurePlayer(voter, now);

  const voteId = makeId("dao-vote", proposalId, voter);
  const vote = VoteRecord.load(voteId);
  if (vote == null) {
    const newVote = new VoteRecord(voteId);
    newVote.proposal = proposalId;
    newVote.voter = voter;
    newVote.direction = "For";
    newVote.power = power;
    newVote.votedAt = now;
    newVote.save();
  }
}

