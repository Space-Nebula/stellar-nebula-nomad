// ─── Helpers ────────────────────────────────────────────────────────────────

function ensurePlayer(id: string, now: BigInt): Player {
  let player = Player.load(id);
  if (player == null) {
    player = new Player(id);
    player.profileId = null;
    player.totalScans = 0;
    player.essenceEarned = BigInt.fromI32(0);
    player.createdAt = now;
    player.lastUpdatedAt = now;
    player.save();
  } else {
    player.lastUpdatedAt = now;
    player.save();
  }
  return player as Player;
}

function ensureShip(
  id: string,
  ownerId: string,
  now: BigInt
): Ship {
  let ship = Ship.load(id);
  if (ship == null) {
    ship = new Ship(id);
    ship.owner = ownerId;
    ship.shipType = "";
    ship.hull = 0;
    ship.scannerPower = 0;
    ship.level = 1;
    ship.scanRange = 0;
    ship.metadataCID = null;
    ship.mintedAt = now;
    ship.save();
  }
  return ship as Ship;
}

function makeId(...parts: string[]): string {
  let joined = parts.join("-");
  let hash = crypto.keccak256(String.UTF8.encode(joined));
  return hash.toHexString();
}

function eventTimestamp(): BigInt {
  // In a real handler, use `block.timestamp` from the event context.
  return BigInt.fromI32(0);
}

// ─── Ship NFT Handlers ─────────────────────────────────────────────────────

/**
 * Handles `("ship", "minted")` → (id: u64, owner: Address, ship_type: Symbol,
 *                                  hull: u32, scanner_power: u32)
 */
export function handleShipMinted(event: any): void {
  const now = eventTimestamp();
  const shipId = event.parameters[0].value.toU64().toString();
  const ownerAddress = event.parameters[1].value.toAddress().toHexString();
  const shipType = event.parameters[2].value.toSymbol().toString();
  const hull = event.parameters[3].value.toU32();
  const scannerPower = event.parameters[4].value.toU32();

  ensurePlayer(ownerAddress, now);
  const ship = ensureShip(shipId, ownerAddress, now);
  ship.shipType = shipType;
  ship.hull = i32(hull);
  ship.scannerPower = i32(scannerPower);
  ship.save();
}

/**
 * Handles `("ship", "xfer")` → (ship_id: u64, from: Address, to: Address)
 */
export function handleShipTransfer(event: any): void {
  const now = eventTimestamp();
  const shipId = event.parameters[0].value.toU64().toString();
  const toAddress = event.parameters[2].value.toAddress().toHexString();

  ensurePlayer(toAddress, now);

  const ship = Ship.load(shipId);
  if (ship != null) {
    ship.owner = toAddress;
    ship.save();
  } else {
    log.warning("Ship {} not found on transfer", [shipId]);
  }
}

